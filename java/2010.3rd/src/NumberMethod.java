public class NumberMethod {
    public static void main(String[] args) {
        Integer num1=new Integer(29);
        System.out.println(num1.intValue());        //return as int value
        System.out.println(num1.doubleValue());     //return as double value

        Double num2=new Double(3.14);
        System.out.println(num2.intValue());        //return as int value
        System.out.println(num2.doubleValue());     //return as double value
    }
}
