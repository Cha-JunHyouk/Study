import java.util.*;

public class Main {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int repeat=sc.nextInt();
        int i, a, b;
        for(i=0;i<repeat;i++) {
            a=sc.nextInt();
            b=sc.nextInt();
            System.out.println(a+b);
        }
    }
}

